        A Hatoslottó Zrt. 100%-os állami tulajdonban lévő nemzeti szerencsejáték-szervező gazdasági társaság, Magyarország 109. legnagyobb vállalata.
        Kínálatunkban egyedül a hatoslottóból áll. Cégünk értékesítési hálózata több mint 1.200 értékesítési ponttal rendelkezik.
        Felelős játékszervezőként törvényes szerencsejáték lehetőséget nyújtunk minden 18 év feletti számára. 
